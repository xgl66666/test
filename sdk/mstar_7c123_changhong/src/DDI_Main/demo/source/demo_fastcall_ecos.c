// fastcall related static functions
static MS_BOOL fastcall_crypt_cal(FastCryptoCalParam* param)
{
    __asm__ __volatile__(
		".arch_extension sec\n\t"
        "ldr r0, =" TEESMC32_OPTEE_FASTCALL_CRYPT_CAL " \n\t"
        "mov r1,%0 \n\t"
        "smc #0 \n\t"
        :
        : "r"(MsOS_MPool_PA2VA((MS_PHY)((MS_U32)param)))
        : "r0","r1"
    );
	return 0;
}

/*
  calculate a SHA256 over the given data area
    pabShaRes               ... 32 byte array to return the SHA-value
    pabData                 ... data to calculate the sha about
    lSize                   ... number of bytes to be hashed

  the function returns 1 on success
*/
int MStar_fastcall_Sha256( unsigned char *pabShaRes,
                      unsigned char *pabData,
                      unsigned int lSize
                    )
{
    if( NULL==pabShaRes || NULL==pabData || 0==lSize ){
        printf("[%s][%d] Invalid Argument!!!\n", __FUNCTION__, __LINE__);
        return MSTAR_RET_FAIL;
    }


	MS_U32  u32MemLen       = (0==(lSize%0x10))? lSize : ((lSize/0x10)+1)*0x10;
	MS_U8   *pu8Buf         = MsOS_AllocateMemory( u32MemLen+SHA256_DIGEST_LEN, gs32NonCachedPoolID);
	MS_U32  u32InputData    = (MS_U32)pu8Buf;
	MS_U32  u32OutputData   = (MS_U32)pu8Buf + u32MemLen;

    if(NULL==pu8Buf){
        printf("[%s][%d] MsOS_AllocateMemory() Fail!!! (size= 0x%x)\n", __FUNCTION__, __LINE__, u32MemLen+SHA256_DIGEST_LEN);
        return MSTAR_RET_FAIL;
    }


	memset( pu8Buf, 0, u32MemLen+SHA256_DIGEST_LEN);
	memcpy( (void*)u32InputData, /*pu8DataBuf*/pabData, lSize);

    /// SHA256 Configuration ///
	memset( &stCryptoParam, 0, sizeof(stCryptoParam));
    // Algo
	stCryptoParam.secure_algo		= ALG_SHA256;
    // InData
    stCryptoParam.input_address		= (MS_U64)u32InputData;
    stCryptoParam.input_size		= lSize;
    // Digest
    stCryptoParam.output_address	= (MS_U64)u32OutputData;
    stCryptoParam.output_size		= SHA256_DIGEST_LEN;
	stCryptoParam.mode				= MODE_DIGEST;

    MsOS_MPool_Dcache_Flush( (MS_U32)&stCryptoParam , sizeof(stCryptoParam));
    MsOS_FlushMemory();

	fastcall_crypt_cal( &stCryptoParam);
	memcpy( pabShaRes, (void*)u32OutputData, SHA256_DIGEST_LEN);
	MsOS_FreeMemory( pu8Buf, gs32NonCachedPoolID);

    return MSTAR_RET_OK;
}

