void showmem(unsigned long offset)
{
	unsigned long addr=0x80000000+offset;
	unsigned char data;
	printf("\nstart memory address : %08X\n",addr);
	int i,j;
	for(i=0;i<16;i++)
	{
		for(j=0;j<16;j++)
		{
			data=(*((volatile unsigned char*)(addr)));
			addr++;
			printf("%02x ",data);
		}
		printf("\n");
	}
}

unsigned int BOOTROM_SecureMain(U8 *au8PublicKey_N, U8 *au8PublicKey_E, U8 *au8Signature, U8 *au8AuthData, U32 u32AuthDataLen)
{
    int i;
    unsigned char digest[SHA256_DIGEST_SIZE];
    unsigned char signature[RSA_KEY_DIGI_LEN*4];

    {
        unsigned int time = 0, difftime = 0;
        time = get_ticks();

        rsa_main(au8Signature, au8PublicKey_N, au8PublicKey_E, signature);

        difftime = (get_ticks()-time)/((1000 * (CFG_HZ / 1000000)));
        printf("RSA performance %08d ms\n",difftime);
    }

#if defined(CONFIG_TITANIA13)
    {
        unsigned int time = 0, difftime = 0, i;
        unsigned int u32SHAInBuf = ((unsigned int)au8AuthData) & (~0xA0000000);
        unsigned int u32SHAOutBuf = 0x200000;

        MDrv_AESDMA_Init(0x00, 0x00, 1);

        time = get_ticks();

        MDrv_SHA_Calculate(E_DRVAESDMA_SHA256, u32SHAInBuf, u32AuthDataLen, u32SHAOutBuf);

        difftime=(get_ticks()-time)/((1000 * (CFG_HZ / 1000000)));
        printf("SHA performance %08d ms\n",difftime);

        u32SHAOutBuf |= 0xA0000000;

        for(i=0; i<SHA256_DIGEST_SIZE; i++)
        {
            digest[i] = *((unsigned char *)u32SHAOutBuf + SHA256_DIGEST_SIZE - i - 1);
        }
    }
#else
    sha256(au8AuthData, u32AuthDataLen, digest);
#endif

#if 0
    printf("\n========================================================\n");
    for(i=0; i<32; i++)
    {
        printf("%02X ", *((unsigned char *)digest + i));
        if(i%16==15)
        {
            printf("\n");
        }
    }
    printf("\n========================================================\n");
    printf("\n========================================================\n");
    for(i=0; i<256; i++)
    {
        printf("%02X ", *((unsigned char *)signature + i));
        if(i%16==15)
        {
            printf("\n");
        }
    }
    printf("\n========================================================\n");
#endif
    for(i = 0; i<SHA256_DIGEST_SIZE; i++)
    {
        if (signature[i] != digest[SHA256_DIGEST_SIZE-i-1])
        {
            printf("%02X %02X\n", signature[i], digest[SHA256_DIGEST_SIZE-i-1]);
            return 1;
        }
    }

    return 0;
}

